<h3 class="paragraphTitle">Course Goals</h3>
The goals of this course include:
<ul>
    <li>Understand how to assure client service, fiduciary responsibility, integrity, transparency, compliance, and accountability in the planning and formation of contracts while, at the same time, securing the best practicable deal for the public entity and its stakeholders.</li>
    <li>Understand why procurement plays a strategic role in planning and forming contracts for public sector organizations focused on results.</li>
    <li>Understand how best-practice strategic procurement planning and contract formation fit into the broader context of strategically-managed public sector organizations.</li>
    <li>Understand plan procurements and form contracts.</li>
    <li>Understand determination, documentation, and communication of requirements for a good, service, or construction work</li>
    <li>Understand the concepts of combination of specification type, contract type, source selection method, incentives, and other strategies for procurements and contracts.</li>
</ul>


<h3 class="paragraphTitle">Course Objectives</h3>
By the end of this course, students should be able to:
<ul>
    <li>Demonstrate a fundamental understanding of the scope and elements of public sector contract planning and formation by properly and adequately answering questions in discussion forums and on exams.</li>
    <li>Apply knowledge gained in the course to properly, adequately, and strategically plan procurements and form contracts by completing case studies and answering essay exam questions.</li>
    <li>Apply their knowledge, their research, and the information learned in the course to a final research paper on an issue of public sector contract planning and formation.</li>
</ul>
